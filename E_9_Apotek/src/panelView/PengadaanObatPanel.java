    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try{
            inputKosongException();
            
            String tanggal = "";
            int kuantitas = 0;
            
            int selectedIndexSupplier = supplierjComboBox3.getSelectedIndex();
            Supplier selectedSupplier = listSupplier.get(selectedIndexSupplier);
            
            int selectedIndexApoteker = apotekerjComboBox2.getSelectedIndex();
            Apoteker selectedApoteker = listApoteker.get(selectedIndexApoteker);
            
            int selectedIndexObat = obatjComboBox1.getSelectedIndex();
            Obat selectedObat = listObat.get(selectedIndexObat);
            
            double totalHarga = Integer.parseInt(jumlahObatField.getText()) * selectedObat.getHarga_obat();
            tanggal = tanggaljTextField1.getText();
            kuantitas = Integer.parseInt(jumlahObatField.getText());
            
            int opsi = JOptionPane.showConfirmDialog(rootPane, "Yakin ingin melakukan " +action+ "?");
            if(opsi == JOptionPane.NO_OPTION || opsi == JOptionPane.CANCEL_OPTION || opsi == JOptionPane.CLOSED_OPTION){
                return;
            }

            if(action.equals("tambah")){
                PengadaanObat po = new PengadaanObat(selectedApoteker.getId_apoteker(), selectedSupplier.getId_supplier(), selectedObat.getId_obat(), selectedApoteker, selectedSupplier, selectedObat, totalHarga, tanggal, kuantitas);
                pengadaanObatControl.insertDataPengadaan(po);
            }else{
                PengadaanObat po = new PengadaanObat(selectedApoteker.getId_apoteker(), selectedSupplier.getId_supplier(), selectedObat.getId_obat(), selectedApoteker, selectedSupplier, selectedObat, totalHarga, tanggal, kuantitas);
                pengadaanObatControl.updatePengadaan(po, selectedId);
                selectedId = -1;
            }
            clearText();
            setComponent(false);
            setEdiDeleteButton(false);
            tableData.setEnabled(true);
            showTableBySearch("");
        }catch(InputKosongException e){
            JOptionPane.showMessageDialog(rootPane, e.message());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(rootPane, "Kuantitas Harus Berupa Angka");
        }
        
    }//GEN-LAST:event_saveButtonActionPerformed

