    private void tableDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDataMouseClicked
        int indexSupplier = -1, indexApoteker = -1, indexObat = -1;
        action = "baharui";
        
        tambahDataButton.setEnabled(false);
        cancelButton.setEnabled(true);
        saveButton.setEnabled(true);
        setEdiDeleteButton(true);
        
        setComponent(false);
        
        int clickedRow = tableData.getSelectedRow();
        TableModel tableModel = tableData.getModel();
        
        selectedId = Integer.parseInt(tableModel.getValueAt(clickedRow, 9).toString());
        
        idPengadaanField.setText(tableModel.getValueAt(clickedRow, 9).toString());
        jumlahObatField.setText(tableModel.getValueAt(clickedRow, 4).toString());
        tanggaljTextField1.setText(tableModel.getValueAt(clickedRow, 3).toString());
        hargajTextArea1.setText("Rp " +tableModel.getValueAt(clickedRow, 5).toString());
        
        for(Supplier supplier : listSupplier){
            if(supplier.getId_supplier() == Integer.parseInt(tableModel.getValueAt(clickedRow, 7).toString())){
                indexSupplier = listSupplier.indexOf(supplier);
            }
        }
        
        for(Apoteker apoteker : listApoteker){
            if(apoteker.getId_apoteker() == Integer.parseInt(tableModel.getValueAt(clickedRow, 8).toString())){
                indexApoteker = listApoteker.indexOf(apoteker);
            }
        }
        
        for(Obat obat : listObat){
            if(obat.getId_obat()== Integer.parseInt(tableModel.getValueAt(clickedRow, 6).toString())){
                indexObat = listObat.indexOf(obat);
            }
        }
        
        supplierjComboBox3.setSelectedIndex(indexSupplier);
        apotekerjComboBox2.setSelectedIndex(indexApoteker);
        obatjComboBox1.setSelectedIndex(indexObat);
        cancelButton.setEnabled(true);
    }//GEN-LAST:event_tableDataMouseClicked

