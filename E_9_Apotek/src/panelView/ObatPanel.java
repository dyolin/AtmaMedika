    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        
        String kategoriObat = "";
        
        try{
            inputKosongException();
            
            double harga = Double.parseDouble(hargaObatField.getText());
            int jumlah = Integer.parseInt(jumlahObatField.getText());
            
            kategoriObat = kategoriGroup.getSelection().getActionCommand();
            
            int opsi = JOptionPane.showConfirmDialog(rootPane, "Yakin Ingin melakukan " +action+ "?");
            if(opsi == JOptionPane.NO_OPTION || opsi == JOptionPane.CANCEL_OPTION || opsi == JOptionPane.CLOSED_OPTION){
                return;
            }
            
            if(action.equals("tambah")){
                Obat obat = new Obat(namaObatField.getText(), kategoriObat, Double.parseDouble(hargaObatField.getText()), Integer.parseInt(jumlahObatField.getText()));
                obatControl.insertDataObat(obat);
            }else{
                Obat obat = new Obat(namaObatField.getText(), kategoriObat, Double.parseDouble(hargaObatField.getText()), Integer.parseInt(jumlahObatField.getText()));
                obatControl.updateObat(obat, Integer.parseInt(idObatField.getText()));
                selectedId = -1;
            }
            
            clearText();
            setComponent(false);
            setEditDeleteButton(false);
            setRadioComponent(false);
            kategoriGroup.clearSelection();
            tambahDataButton.setEnabled(true);
            showTableBySearch("");
        }catch(InputKosongException e){
            JOptionPane.showMessageDialog(rootPane, e.message());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(rootPane, "Harga dan Jumlah Obat Harus Angka");
        }
        
    }//GEN-LAST:event_saveButtonActionPerformed

