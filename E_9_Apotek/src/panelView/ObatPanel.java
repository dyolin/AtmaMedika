    private void tableDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDataMouseClicked
        action = "baharui";
        
        tambahDataButton.setEnabled(false);
        cancelButton.setEnabled(true);
        saveButton.setEnabled(true);
        setEditDeleteButton(true);
        
        setComponent(false);
        setRadioComponent(false);
        kategoriGroup.clearSelection();
        
        int clickedRow = tableData.getSelectedRow();
        TableModel tableModel = tableData.getModel();
        
        selectedId = Integer.parseInt(tableModel.getValueAt(clickedRow, 0).toString());
        
        idObatField.setText(tableModel.getValueAt(clickedRow, 0).toString());
        namaObatField.setText(tableModel.getValueAt(clickedRow, 1).toString());
        hargaObatField.setText(tableModel.getValueAt(clickedRow, 3).toString());
        jumlahObatField.setText(tableModel.getValueAt(clickedRow, 4).toString());
        
        String kategoriObat = tableModel.getValueAt(clickedRow, 2).toString();
        
        switch(kategoriObat){
            case "Obat Bebas":
                obatBebasButton.setSelected(true);
                break;
            case "Obat Bebas Terbatas":
                obatBebasTerbatasButton.setSelected(true);
                break;
            case "Obat Keras":
                obatKerasButton.setSelected(true);
                break;
            case "Obat Psikotropika":
                obatPsikotropikaButton.setSelected(true);
                break;
            case "Obat Narkotika":
                obatPsikotropikaButton.setSelected(true);
        }
        
        cancelButton.setEnabled(true);
    }//GEN-LAST:event_tableDataMouseClicked

