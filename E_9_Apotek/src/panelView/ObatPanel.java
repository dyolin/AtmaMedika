    private void hapusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusButtonActionPerformed
        action = "hapus";
        
        int opsi = JOptionPane.showConfirmDialog(rootPane, "Yakin Ingin Hapus ?", "Hapus Data", JOptionPane.YES_NO_CANCEL_OPTION);
        if(opsi == JOptionPane.NO_OPTION || opsi == JOptionPane.CANCEL_OPTION || opsi == JOptionPane.CLOSED_OPTION){
            return;
        }
        
        obatControl.deleteObat(selectedId);
        
        clearText();
        setComponent(false);
        setEditDeleteButton(false);
        setRadioComponent(false);
        kategoriGroup.clearSelection();
        tambahDataButton.setEnabled(true);
        showTableBySearch("");
        
    }//GEN-LAST:event_hapusButtonActionPerformed

